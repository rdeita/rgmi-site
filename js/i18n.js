const I18N = {
  current: localStorage.getItem('rgmi_lang') || 'es',

  init() {
    this.apply(this.current);
    this.bindToggle();
  },

  bindToggle() {
    const toggle = document.getElementById('langToggle');
    if (!toggle) return;
    toggle.addEventListener('click', () => {
      this.set(this.current === 'es' ? 'en' : 'es');
    });
  },

  set(lang) {
    this.current = lang;
    localStorage.setItem('rgmi_lang', lang);
    this.apply(lang);
    document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
  },

  t(key) {
    return TRANSLATIONS[this.current]?.[key] || TRANSLATIONS.es[key] || key;
  },

  apply(lang) {
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const text = TRANSLATIONS[lang]?.[key];
      if (text === undefined) return;
      if (el.tagName === 'META' || el.hasAttribute('content')) {
        el.setAttribute('content', text);
      } else {
        el.innerHTML = text;
      }
    });

    document.querySelectorAll('[data-i18n-list]').forEach(el => {
      const key = el.getAttribute('data-i18n-list');
      const items = TRANSLATIONS[lang]?.[key];
      if (!Array.isArray(items)) return;
      el.innerHTML = items.map(li => `<li>${li}</li>`).join('');
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      const text = TRANSLATIONS[lang]?.[key];
      if (text) el.placeholder = text;
    });

    const toggle = document.getElementById('langToggle');
    if (toggle) {
      const cur = toggle.querySelector('.lang-current');
      const oth = toggle.querySelector('.lang-other');
      if (cur && oth) {
        cur.textContent = lang.toUpperCase();
        oth.textContent = lang === 'es' ? 'EN' : 'ES';
      }
    }
  }
};

document.addEventListener('DOMContentLoaded', () => I18N.init());
window.I18N = I18N;
